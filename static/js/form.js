//  To calculate the Time Duration

function shownumberofdays() {
    var start_date = document.getElementById("start-date").value;
    var end_date = document.getElementById("end-date").value;
    var calculate_time = document.getElementById('days');

    if (start_date && end_date) {
        console.log("Start Date: ", start_date, " End Date: ", end_date); // Debugging
        
        var startDate = new Date(start_date);
        var endDate = new Date(end_date);
        var TimeDifference = endDate - startDate;
        var TotalDays = Math.ceil(TimeDifference / (1000 * 3600 * 24));
        
        console.log("Total Days: ", TotalDays); // Debugging
        
        if (TotalDays > 0) {
            calculate_time.value = TotalDays;
        } else {
            calculate_time.value = '0';
        }
    } else {
        calculate_time.value = "";
    }
}
